char	*ft_strcapitalize(char *str)
{
	int	i;
	int	j;

	i = 0;
	if (str[0] >= 'a' && str[0] <= 'z')
	{
		str[0] -= 32;
	}
	j = 1;
	while (str[j] != '\0')
	{
		if (str[j] >= 'A' && str[j] <= 'Z')
		{
			str[j] += 32;
			i++;
		}
		if (str[j] >= 'a' && str[j] <= 'z')
			if (!(str[j - 1] >= 'a' && str[j - 1] <= 'z'))
				if (!(str[j - 1] >= '0' && str[j - 1] <= '9'))
					if (!(str[j - 1] >= 'A' && str[j - 1] <= 'Z'))
						str[j] -= 32;
		j++;
	}
	return (str);
}
