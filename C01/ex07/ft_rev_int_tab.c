void	ft_rev_int_tab(int *tab, int size)
{
	int	i;
	int	j;
	int	empty;

	i = 0;
	j = size - 1;
	while (i < j)
	{
		empty = tab[i];
		tab[i] = tab[j];
		tab[j] = empty;
		i++;
		j--;
	}
}
